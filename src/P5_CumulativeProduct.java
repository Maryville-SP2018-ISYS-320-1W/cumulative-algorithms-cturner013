public class P5_CumulativeProduct {

	public static void main(String[] args) {
	}
	public static int factorial (int n) {
		int product = 1;
		for (int i = 2; i <= n; i++) {
			product = product * i;
			for (int i1 = 0; i1 <= 10; i1 ++) {
				System.out.println(i1 + "! = " + factorial(i1));

		}
		return product;
	}
		return n;
	}
}

	
	
	
	





